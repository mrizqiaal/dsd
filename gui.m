% --- Executes on button press in pushbutton3.
function pushbutton3_Callback(hObject, eventdata, handles)
str = get(handles.edit9, 'string');
[row col] = size(str);

% Mengubah str ke cell agar trailing space hilang dan support konkatenasi
outStr = [];
for i = 1 : row
   outStr = [outStr; {deblank(str(i,:))}];
end

% Fungsi sha256 hanya menerima input string, sementara outStr bertipe cell
% Kode di bawah digunakan untuk mengubah outStr menjadi string
str4Hash = outStr{1};
for i = 2 : row
    str4Hash = [str4Hash char(10) outStr{i}];
end

hash = sha256(str4Hash);
set(handles.edit11, 'string', hash);
if ismac
    [status,cmd1] = unix('ifconfig en0 | awk ''/ether/{print $2}''');
elseif ispc
    [status,cmd1] = dos('getmac');
    cmd1 = cmd1(160:176);
end
plain = abs(preprocessing(hash));
key = get(handles.edit13, 'string');
key = abs(padding(key));

[sBox, polyMat, rcon, leftShift] = getTable();
mac = macGeneration();
sBox = sBoxGenerator(sBox, mac);

roundkey = keyExpand(key, sBox, rcon);

[r, c] = size(plain);

all_plain = [];
all_encrypt = [];
all_char = [];
for i = 1:r
    all_plain = [all_plain, cipher2hex(plain(i,:))];
    encrypted = encryption(plain(i,:), roundkey, sBox, leftShift, polyMat);
    all_char = [all_char, char(reshape(encrypted, 1, 16))];
    encrypted_in_hex = cipher2hex(encrypted);
    all_encrypt = [all_encrypt, encrypted_in_hex];
end

%hexArray = strsplit(all_char, ' ');
all_encrypt
set(handles.edit10, 'string', cmd1);

outStr = [outStr; {'--- BEGIN SIGNATURE ---'; all_encrypt; '---- END SIGNATURE ----'}];
set(handles.edit12, 'Max', 2, 'String', outStr);

